package com.samik.newAdmission;

public class StudentExchangeDetail {
	private String studentExchangeName;
	private int studentExchangeFees;
	public String getStudentExchangeName() {
		return studentExchangeName;
	}
	public void setStudentExchangeName(String studentExchangeName) {
		this.studentExchangeName = studentExchangeName;
	}
	public int getStudentExchangeFees() {
		return studentExchangeFees;
	}
	public void setStudentExchangeFees(int studentExchangeFees) {
		this.studentExchangeFees = studentExchangeFees;
	}
}
